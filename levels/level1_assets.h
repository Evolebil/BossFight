//
// Светлая грань кирпичных тайлов в PNG смотрит ВЛЕВО и ВВЕРХ.
// Это значит:
//
//   mww0000  — грань слева  → ставим на ПРАВУЮ стену арены
//   mww0001  — грань справа (flip H от mww0000) → ЛЕВАЯ стена арены
//   mwf0000  — грань сверху → ПОТОЛОК (грань смотрит внутрь вниз) ✓
//   mwf0001  — грань снизу (flip V) → ПОЛ (грань смотрит внутрь вверх) ✓
//
//   Углы (грань смотрит внутрь арены):
//   mwc0000 (0°)   → угол верх-ПРАВО арены  (грань вверх+влево)
//   mwc0010 (90°)  → угол верх-ЛЕВО арены
//   mwc0020 (180°) → угол низ-ПРАВО арены
//   mwc0030 (270°) → угол низ-ЛЕВО арены

// ============================================================
// СТРУКТУРА ТАЙЛА
// ============================================================

